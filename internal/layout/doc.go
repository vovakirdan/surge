// Package layout вычисляет ABI layout (размер и выравнивание) типов для конкретной целевой платформы.
//
// Пакет используется компилятором и виртуальной машиной Surge для определения:
//   - размера типов в памяти (Size)
//   - выравнивания типов (Align)
//   - смещений полей в структурах (FieldOffsets)
//   - layout'а tagged union'ов (TagSize, TagAlign, PayloadOffset)
//
// Основные компоненты:
//
//   - LayoutEngine: движок для вычисления layout'ов типов с кэшированием результатов
//   - TypeLayout: результат вычисления layout'а типа (размер, выравнивание, смещения полей)
//   - Target: описание целевой платформы (размер и выравнивание указателей)
//
// Поддерживаемые типы:
//
//   - Скалярные типы: bool, int/uint/float различных размеров
//   - Составные типы: структуры, кортежи, массивы, tagged union'ы
//   - Ссылочные типы: указатели, ссылки, функции
//   - Специальные типы: unit, nothing, enum
//
// Атрибуты layout'а:
//
//   - @packed: упакованная структура без выравнивания (align=1, поля идут последовательно)
//   - @align(N): явное указание выравнивания для типа или поля
//
// Особенности:
//
//   - Кэширование: результаты вычисления layout'ов кэшируются для повышения производительности
//   - Обнаружение рекурсии: выявляет рекурсивные типы с бесконечным размером (LayoutErrRecursiveUnsized)
//   - Канонизация типов: разрешает алиасы и own-типы перед вычислением layout'а
//   - Поддержка платформ: в текущей версии поддерживается только x86_64-linux-gnu
//
// Использование:
//
//   - New: создание LayoutEngine для целевой платформы
//   - LayoutOf: получение полного layout'а типа
//   - SizeOf, AlignOf: получение размера и выравнивания типа
//   - FieldOffset: получение смещения поля в структуре
//
// Пакет следует ABI layout контракту v1 для x86_64-linux-gnu, обеспечивая совместимость
// между виртуальной машиной и будущими бэкендами (например, LLVM).
