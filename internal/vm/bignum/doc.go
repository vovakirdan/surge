// Package bignum реализует арифметику произвольной точности для больших чисел,
// используемую виртуальной машиной Surge для работы с числовыми типами.
//
// Пакет предоставляет три основных типа:
//
//   - BigUint: беззнаковые целые числа произвольной точности
//   - BigInt: знаковые целые числа произвольной точности
//   - BigFloat: числа с плавающей точкой произвольной точности
//
// Представление чисел:
//
//   - BigUint и BigInt используют представление в виде limbs (база 2^32, little-endian).
//     Ноль представляется как пустой срез limbs. BigInt дополнительно хранит флаг знака.
//   - BigFloat использует нормализованную форму: (-1)^Neg * Mant * 2^Exp,
//     где Mant — мантисса (BigUint), Exp — экспонента (int32).
//     Мантисса имеет фиксированную точность (MantissaBits = 256 бит).
//
// Арифметические операции:
//
//   - Целые числа: сложение (Add), вычитание (Sub), умножение (Mul), деление (DivMod),
//     битовые сдвиги (Shl, Shr), сравнение (Cmp)
//   - Числа с плавающей точкой: сложение (FloatAdd), вычитание (FloatSub),
//     умножение (FloatMul), деление (FloatDiv), сравнение (Cmp)
//   - Преобразования между типами: FloatFromUint, FloatFromInt, FloatToIntTrunc, FloatToUintTrunc
//
// Парсинг и форматирование:
//
//   - ParseUintLiteral, ParseIntLiteral: парсинг числовых литералов (поддерживает префиксы 0x, 0b, 0o)
//   - ParseUint, ParseInt, ParseFloat: парсинг строк с числовыми значениями
//   - FormatUint, FormatInt, FormatFloat: форматирование чисел в строковое представление
//
// Ограничения:
//
//   - Максимальный размер числа ограничен константой MaxLimbs (1_000_000 limbs),
//     что соответствует примерно 32 миллионам бит или 4 миллионам байт
//   - При превышении лимита операции возвращают ошибку ErrMaxLimbs
//   - Деление на ноль возвращает ErrDivByZero
//   - Вычитание с переполнением для беззнаковых чисел возвращает ErrUnderflow
//
// Пакет используется виртуальной машиной для выполнения операций над числами,
// размер которых превышает стандартные типы Go (int64, uint64, float64).
