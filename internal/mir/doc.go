// Package mir реализует промежуточное представление (Middle Intermediate Representation, MIR)
// для компилятора Surge.
//
// MIR представляет код в виде графа потока управления (CFG) с базовыми блоками и инструкциями.
// Это низкоуровневое представление, полученное из HIR (High Intermediate Representation)
// путём lowering'а высокоуровневых конструкций в простые операции.
//
// Основные компоненты:
//
//   - Module: модуль, содержащий функции и метаданные
//   - Func: функция с локальными переменными, базовыми блоками и точкой входа
//   - Block: базовый блок с последовательностью инструкций и терминатором
//   - Instr: инструкция (присваивание, вызов функции, drop, await, spawn и т.д.)
//   - Operand: операнд (константа, копия, перемещение, взятие адреса)
//   - Place: место в памяти (локальная переменная с возможными проекциями)
//   - RValue: правостороннее значение (использование, унарные/бинарные операции, литералы и т.д.)
//   - Terminator: терминатор блока (return, goto, if, switch_tag, unreachable)
//
// Процесс преобразования:
//
//   - LowerModule: преобразует моно-HIR модуль в MIR модуль
//   - lowerFunc: преобразует HIR функцию в MIR функцию с базовыми блоками
//   - lowerBlock/lowerStmt/lowerExpr: рекурсивное преобразование блоков, statements и выражений
//
// Оптимизации и валидация:
//
//   - SimplifyCFG: упрощает граф потока управления (удаляет тривиальные goto, недостижимые блоки)
//   - Validate: проверяет инварианты MIR модуля (терминация блоков, корректность типов и т.д.)
//   - DumpModule: выводит человекочитаемое представление модуля для отладки
//
// Специальные функции:
//
//   - BuildSurgeStart: создаёт синтетическую функцию __surge_start для точки входа программы
//
// MIR использует SSA-подобную структуру, где каждое значение имеет уникальный идентификатор,
// а операции явно разделены на инструкции и терминаторы для упрощения последующих оптимизаций
// и генерации кода.
