// Package symbols builds and owns the symbol table used by later semantic passes.
//
// # Назначение
// Поддержка областей видимости: файл → модуль → функции → блоки. Пакет хранит
// объявления, ссылки на них и их местоположения в исходниках. Он предоставляет
// API резолвера, который используется семантикой для проходов.
//
// # Не делает
// Не лезет в типы, не анализирует выражения и не взаимодействует с IO/CLI.
// Форматирование и вывод диагностик остаются за внешними пакетами.
//
// # Хранит
// Арену символов и областей, индексы `ScopeID`/`SymbolID`, ссылки на атрибуты,
// спаны объявлений и таблицы для быстрых поисков по именам.
//
// # Зависимости
// Только `ast` для обходов, `source` для работы со спанами и `diag` для
// формирования сообщений об ошибках.
package symbols
