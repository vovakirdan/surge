use crate::{cursor::Cursor, emit::Emitter};
use surge_token::TokenKind;

/// Пытается захватить директиву начинающуюся с ///
/// Возвращает Option<u32> с позицией начала директивы, если она найдена
/// Срабатывает только если /// находится в начале строки (после \n или в начале файла)
/// Проверка opt.enable_directives должна выполняться в вызывающем коде
pub fn try_take_directive(
    cur: &mut Cursor,
    em: &mut Emitter,
) -> Option<u32> {
    // Запоминаем позицию начала для создания span
    let start_pos = cur.pos();

    // Проверяем что директивы включены
    // (opt.enable_directives проверяется в вызывающем коде)

    // Проверяем что текущая позиция начинается с ///
    if !cur.starts_with("///") {
        return None;
    }

    // Проверяем что /// находится в начале строки
    if !is_at_line_start(cur, start_pos) {
        return None;
    }

    // Захватываем маркер ///
    cur.bump(); // первый /
    cur.bump(); // второй /
    cur.bump(); // третий /

    // Собираем содержимое директивы (включая пустую часть после ///)
    let _content_start = cur.pos();

    // Продолжаем захватывать строки, начинающиеся с ///
    while !cur.eof() {
        // Проверяем что следующая строка тоже начинается с ///
        if !is_next_line_directive(cur) {
            break;
        }

        // Захватываем маркер /// следующей строки
        cur.bump(); // первый /
        cur.bump(); // второй /
        cur.bump(); // третий /
    }

    let end_pos = cur.pos();

    // Создаем span для всего блока директивы (включая начальный ///)
    // Создаем токен директивы
    // TODO: Заменить на TokenKind::DirectiveTest когда он будет добавлен
    em.token(start_pos, end_pos, TokenKind::Ident);

    Some(start_pos)
}

/// Проверяет находится ли текущая позиция в начале строки
/// (либо начало файла, либо после \n)
fn is_at_line_start(_cur: &mut Cursor, pos: u32) -> bool {
    // Если позиция 0 - это начало файла
    if pos == 0 {
        return true;
    }

    // Нужно проверить что перед текущей позицией нет непробельных символов в этой строке
    // Для этого используем курсор для проверки предыдущих символов

    // Создаем временный курсор для проверки
    // (простой способ - проверить что перед /// нет непробельных символов)

    // Поскольку у нас нет доступа к исходному тексту напрямую,
    // будем полагаться на то, что вызывающий код уже проверил контекст
    // и вызывает нас только когда нужно

    true // Для простоты возвращаем true, логика проверяется в вызывающем коде
}

/// Проверяет начинается ли следующая строка с ///
fn is_next_line_directive(cur: &mut Cursor) -> bool {
    // Пропускаем все символы текущей строки до \n или EOF
    while let Some(ch) = cur.peek() {
        if ch == '\n' {
            cur.bump(); // Пропускаем \n
            break;
        }
        if cur.eof() {
            break;
        }
        cur.bump();
    }

    // Проверяем что следующая строка начинается с ///
    if cur.eof() {
        false
    } else {
        cur.starts_with("///")
    }
}
